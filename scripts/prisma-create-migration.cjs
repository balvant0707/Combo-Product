#!/usr/bin/env node

const { spawnSync } = require("node:child_process");
const { existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } = require("node:fs");
const { join } = require("node:path");

function timestamp() {
  const now = new Date();
  const pad = (value) => String(value).padStart(2, "0");
  return [
    now.getFullYear(),
    pad(now.getMonth() + 1),
    pad(now.getDate()),
    pad(now.getHours()),
    pad(now.getMinutes()),
    pad(now.getSeconds()),
  ].join("");
}

function sanitizeName(name) {
  return name
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "");
}

function loadDatabaseUrlFromDotEnv(projectRoot) {
  const envPath = join(projectRoot, ".env");
  if (!existsSync(envPath)) return undefined;

  const lines = readFileSync(envPath, "utf8").split(/\r?\n/);
  const databaseUrlLine = lines.find((line) => line.startsWith("DATABASE_URL="));
  if (!databaseUrlLine) return undefined;

  const raw = databaseUrlLine.slice("DATABASE_URL=".length).trim();
  return raw.replace(/^"(.*)"$/, "$1");
}

const migrationNameInput = process.argv.slice(2).join(" ");
const migrationName = sanitizeName(migrationNameInput);

if (!migrationName) {
  console.error("Usage: npm run migrate:create -- <migration_name>");
  process.exit(1);
}

const projectRoot = process.cwd();
const databaseUrl = process.env.DATABASE_URL || loadDatabaseUrlFromDotEnv(projectRoot);

if (!databaseUrl) {
  console.error("DATABASE_URL is required.");
  process.exit(1);
}

const migrationsDir = join(projectRoot, "prisma", "migrations");
const migrationDir = join(migrationsDir, `${timestamp()}_${migrationName}`);
const lockFile = join(migrationsDir, "migration_lock.toml");
const migrationFile = join(migrationDir, "migration.sql");

function quoteForShell(value) {
  return `"${String(value).replace(/"/g, '\\"')}"`;
}

if (!existsSync(lockFile)) {
  writeFileSync(
    lockFile,
    "# Please do not edit this file manually\n# It should be added in your version-control system (e.g. Git)\nprovider = \"mysql\"\n",
    "utf8",
  );
}

mkdirSync(migrationDir, { recursive: true });

const command = [
  "prisma",
  "migrate",
  "diff",
  "--from-url",
  quoteForShell(databaseUrl),
  "--to-schema-datamodel",
  quoteForShell("prisma/schema.prisma"),
  "--script",
].join(" ");

const result = spawnSync(command, {
  cwd: projectRoot,
  encoding: "utf8",
  env: { ...process.env, DATABASE_URL: databaseUrl },
  shell: true,
});

if (result.error) {
  rmSync(migrationDir, { recursive: true, force: true });
  console.error(result.error.message);
  process.exit(1);
}

if (result.status !== 0) {
  rmSync(migrationDir, { recursive: true, force: true });
  console.error(result.stderr || result.stdout || "Failed to generate migration diff.");
  process.exit(result.status || 1);
}

const sql = (result.stdout || "").trim();
const sqlWithoutComments = sql
  .split(/\r?\n/)
  .map((line) => line.trim())
  .filter((line) => line && !line.startsWith("--"))
  .join("\n")
  .trim();

if (!sqlWithoutComments) {
  rmSync(migrationDir, { recursive: true, force: true });
  console.log("No schema changes detected. Migration not created.");
  process.exit(0);
}

writeFileSync(migrationFile, `${result.stdout.trimEnd()}\n`, "utf8");

console.log(`Created migration: prisma/migrations/${migrationDir.split(/[/\\\\]/).pop()}/migration.sql`);
console.log("Apply it with: npm run migrate:deploy");
